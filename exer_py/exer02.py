'''
E01 - Qual o peso somado?
José pesa 80Kg e Maria pesa 55Kg.

Qual o peso somado dos dois?

Escreva um código que:

Armazena o peso de José na variável peso_jose e o peso de Maria na variável peso_maria.
Calcula a soma dos pesos dos dois e armazena o resultado na variável soma_pesos.
Imprima a soma dos pesos, contida na variável soma_pesos.

'''

peso_jose = 80 
peso_maria = 55

soma = peso_jose + peso_maria

print("A soma do peso de José e Maria é igual:", soma, "kg")

'''

E02 - Qual a diferença de idade?
A mãe do João tem 50 anos e o João tem 20 anos. Qual a diferença de idade entre eles?

'''

idade_mae_joao = 50
idade_joao = 20
diferenca_idade = idade_mae_joao - idade_joao
print("A diferença de idade entre João e sua mãe é de:", diferenca_idade, "anos")

'''
E03 - Qual foi o valor total das vendas?
Um comerciante vendeu 30 unidades de um produto que custa R$ 5. Qual foi o valor total das vendas?

Faça um código que:

Armazena a quantidade de unidades vendidas do produto em uma variável e o valor do produto em outra variável;
Calcula o resultado do valor total das suas vendas e armazena esse valor em uma terceira variável;
Imprima o resultado total;

'''
unidades = 30
produto = 5
valor_total = unidades * produto
print("O valor total des vendas foi de:", valor_total)

'''
E04 - Quanto o funcionário ganha por hora trabalhada?
Um funcionário ganha R$ 1.000/mês e trabalha 160 horas/mês. Quanto esse funcionário ganha por hora trabalhada?

'''

salario_func = 1.000
horas_trabalhadas_mes = 160
valor_hora = salario_func / horas_trabalhadas_mes
print("O funcionário ganha", valor_hora, "por hora")



